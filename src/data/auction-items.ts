export function getAuctionItems() {
  return auctionItems.filter(i => i.status === 'live' && i.auctionType !== 'buy_now');
}

export function getBuyNowItems() {
  return auctionItems.filter(i => i.status === 'live' && (i.auctionType === 'buy_now' || i.auctionType === 'both'));
}

export function getFeaturedItems() {
  return auctionItems.filter(i => i.featured && i.status === 'live');
}

export function getItemBySlug(slug: string) {
  return auctionItems.find(i => i.slug === slug);
}

